package Parciales;

public class recinto {
	String name;
	int time;
	
	public recinto(String nam,int tim) {
		this.name = nam;
		this.time = tim;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	

}
